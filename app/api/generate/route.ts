function getDefaultSecCnt(ch: string, secCnt: number): number {
  if (secCnt && secCnt > 0) return Math.min(secCnt, 50);
  if (ch === '와디즈') return 14;
  if (ch === '자사몰') return 10;
  if (ch === '쿠팡')   return 8;
  return 10;
}

