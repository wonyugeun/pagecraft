/**
 * 모든 카테고리에 공통 사실 가드(universalFactGuard)를 적용하고,
 * 카테고리별 법규 가드가 있으면(현재 화장품) 그 뒤에 이어붙인다.
 * 카테고리 슬래시 변형(예: "화장품/스킨케어")도 첫 토큰 기준으로 정규화해 매칭.
 */
export function getCategoryCopyGuard(cat: string): string {
  const normCat = cat?.split('/')[0]?.trim() ?? '';
  return universalFactGuard + (COPY_GUARDS[normCat] ?? '');
}
